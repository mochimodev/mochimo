int write2file(char *fname, const void *data, size_t len)
{
   FILE *fp;
   int ecode;

   ecode = VEOK;
   fp = fopen(fname, "wb");
   if (fp == NULL) return VERROR;
   if (len && fwrite(data, len, 1, fp) != 1) ecode = VERROR;
   fclose(fp);

   return ecode;
}

